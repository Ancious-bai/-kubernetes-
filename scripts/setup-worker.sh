#!/bin/bash
#=============================================================================
# 脚本名称: setup-worker.sh
# 脚本用途: 将 Worker 节点加入集群
# 使用方法:
#   方式1: 直接执行 ./setup-worker.sh <join_command>
#   方式2: 手动执行 kubeadm join ...
#=============================================================================

set -e

# 颜色定义
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

echo -e "${GREEN}========================================${NC}"
echo -e "${GREEN}  开始将 Worker 节点加入集群${NC}"
echo -e "${GREEN}========================================${NC}"

# 获取当前主机名
CURRENT_HOSTNAME=$(hostname)
echo -e "${YELLOW}当前主机: $CURRENT_HOSTNAME${NC}"

#=========================================================================
# 检查参数
#=========================================================================
if [ -z "$1" ]; then
    echo ""
    echo -e "${RED}错误: 请提供 join 命令！${NC}"
    echo ""
    echo -e "${YELLOW}使用方法:${NC}"
    echo "  $0 <join_command>"
    echo ""
    echo -e "${YELLOW}示例:${NC}"
    echo "  $0 kubeadm join 192.168.100.10:6443 --token xxx --discovery-token-ca-cert-hash sha256:xxx"
    echo ""
    echo -e "${YELLOW}或者直接在终端执行 Master 节点生成的 join 命令${NC}"
    exit 1
fi

#=========================================================================
# 执行 join
#=========================================================================
echo -e "${GREEN}[1/1] 执行 kubeadm join...${NC}"
echo -e "${YELLOW}命令: $1${NC}"

# 执行 join 命令
$1

#=========================================================================
# 完成
#=========================================================================
echo ""
echo -e "${GREEN}========================================${NC}"
echo -e "${GREEN}  Worker 节点已成功加入集群！${NC}"
echo -e "${GREEN}========================================${NC}"
echo ""
echo -e "${YELLOW}现在回到 Master 节点验证:${NC}"
echo "  kubectl get nodes"
echo ""
echo -e "${YELLOW}应该能看到所有 3 个节点 (1 master + 2 worker)${NC}"
echo ""
